package com.hongcheol.study.ch02;

public class FloatDoubbleExample {

	public static void main(String[] args) {
		
		
		double var1 =3.14;	// double를 더 많이 사용하는 이유는 정확도가 높고 F를 붙히는 번거로움이 없다
		float var2 = 3.14f; //float은 항상 마지막에 대문자 소문자 상관없이 f를 붙여야한다
		float var3 = 3.14F;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
	}

}
